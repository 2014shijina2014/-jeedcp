package com.jeedcp.entity.user;

/**
 * user entity for activiti node.
 * 
 * @author Jeffen@pactera
 * @date 2016/1/4
 * @since v1.2.10
 * 
 */
public class UserEntity {

	protected String id;
	protected String loginName;// 登录名
	protected String name;// 姓名
	protected String email;// 邮箱
	protected String mobile;// 手机
	protected String no;// 工号
	protected String companyName;// 归属公司
	protected String officeName;// 归属部门
	protected String postName;// 归属职务
	protected String companyId;// 归属公司ID
	protected String officeId;// 归属部门ID
	protected String postId;// 归属职务ID
	protected String floorId;// 所属楼层ID

	public UserEntity() {
	}

	public UserEntity(String id) {
		this.id = id;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getLoginName() {
		return loginName;
	}

	public void setLoginName(String loginName) {
		this.loginName = loginName;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public String getCompanyName() {
		return companyName;
	}

	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}

	public String getOfficeName() {
		return officeName;
	}

	public void setOfficeName(String officeName) {
		this.officeName = officeName;
	}

	public String getPostName() {
		return postName;
	}

	public void setPostName(String postName) {
		this.postName = postName;
	}

	public String getCompanyId() {
		return companyId;
	}

	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}

	public String getOfficeId() {
		return officeId;
	}

	public void setOfficeId(String officeId) {
		this.officeId = officeId;
	}

	public String getPostId() {
		return postId;
	}

	public void setPostId(String postId) {
		this.postId = postId;
	}

	public String getFloorId() {
		return floorId;
	}

	public void setFloorId(String floorId) {
		this.floorId = floorId;
	}

}
